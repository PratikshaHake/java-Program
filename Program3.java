/*Display 5 to 1 numbers */
class Demo
{
    void Display()
    {
        int i=0;
        i=5;
        while(i>0)
        {
            System.out.println(i);
            i--;
        }
    }
}
public class Program3 {
    public static void main(String arg[])
    {
        Demo dobj=new Demo();
        dobj.Display();

    }
    
}
